int		ft_intlen(int n)
{
	int len;

	len = 1;
	if (n == -2147483648)
		return (10);
	if (n < 0)
	{
		len++;
		n = -n;
	}
	if (n <= 2147483647)
	{
		while (n > 10)
		{
			len++;
			n /= 10;
		}
	}
	else
		len = 0;
	return (len);
}
